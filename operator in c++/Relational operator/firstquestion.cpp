#include<iostream>
using namespace std;//RELATIONAL OPERATORS- >,>=,<,<=,==,!=
int main(){
int x=3;
int y=5;
//IF CONDITION IS TRUE THE PRINT "0" OTHERWISE "1"
cout<<(x==y)<<endl;//FALSE(0)  (3 IS NOT EQUAL WITH 5)
cout<<(x!=y)<<endl;//TRUE(1)  (3 IS NOT EQUAL TO 5)
cout<<(x>y)<<endl;//FALSE(0)   (3 IS NOT GREATER THAN 5)
cout<<(x>=y)<<endl;//FALSE(0)
cout<<(x<y)<<endl;//TRUE(1)
cout<<(x<=y)<<endl;//TRUE(1)

}